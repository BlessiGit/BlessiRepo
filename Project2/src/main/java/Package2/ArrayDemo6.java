package Package2;

public class ArrayDemo6 {

	public static void main(String[] args) {
		String[][] arr = {{"ann","hanna"},{"jithu","ron"}};
		for(int i=0;i<arr.length;i++) {
			for(int j=0;j<arr.length;j++)
			{
				System.out.print(arr[i][j] + " ");
			}
		
		
		System.out.println();
			
		
	}
}
}



